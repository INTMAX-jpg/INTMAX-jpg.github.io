-- Run this once in Supabase SQL Editor.
-- It stores anonymous visit analytics sent through the track-visit Edge Function.
-- The frontend never writes to this table directly; the Edge Function uses the service role key.

create table if not exists public.visit_analytics (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  event_type text not null default 'page_view' check (event_type in ('page_view', 'gallery_load')),
  visitor_id text not null check (char_length(visitor_id) between 8 and 120),
  session_id text check (session_id is null or char_length(session_id) between 8 and 120),
  page_path text not null default '/',
  page_url text,
  referrer text,
  ip_hash text,
  country text,
  region text,
  city text,
  device_type text not null default 'unknown' check (device_type in ('desktop', 'mobile', 'tablet', 'unknown')),
  os_name text,
  os_version text,
  browser_name text,
  browser_version text,
  user_agent text,
  language text,
  timezone text,
  screen_width integer,
  screen_height integer,
  viewport_width integer,
  viewport_height integer,
  pixel_ratio numeric,
  gallery_load_ms integer,
  gallery_image_total integer,
  gallery_image_loaded integer,
  gallery_image_failed integer,
  metadata jsonb not null default '{}'::jsonb
);

create index if not exists visit_analytics_created_at_idx
  on public.visit_analytics (created_at desc);

create index if not exists visit_analytics_event_created_at_idx
  on public.visit_analytics (event_type, created_at desc);

create index if not exists visit_analytics_visitor_created_at_idx
  on public.visit_analytics (visitor_id, created_at desc);

create index if not exists visit_analytics_page_created_at_idx
  on public.visit_analytics (page_path, created_at desc);

create index if not exists visit_analytics_ip_hash_created_at_idx
  on public.visit_analytics (ip_hash, created_at desc);

alter table public.visit_analytics enable row level security;

-- No anon/authenticated insert policy is created on purpose.
-- Inserts should go through the Edge Function with SUPABASE_SERVICE_ROLE_KEY.

create table if not exists public.visit_ip_geo_cache (
  ip_hash text primary key,
  created_at timestamptz not null default now(),
  last_seen_at timestamptz not null default now(),
  country text,
  region text,
  city text,
  country_code text,
  geo_provider text,
  metadata jsonb not null default '{}'::jsonb
);

create index if not exists visit_ip_geo_cache_last_seen_at_idx
  on public.visit_ip_geo_cache (last_seen_at desc);

create index if not exists visit_ip_geo_cache_country_city_idx
  on public.visit_ip_geo_cache (country, city);

alter table public.visit_ip_geo_cache enable row level security;

-- No anon/authenticated policy is created on purpose.
-- This table is written only by the Edge Function service role and stores no raw IP addresses.

create or replace view public.visit_analytics_daily_summary as
select
  date_trunc('day', created_at) as day,
  count(*) filter (where event_type = 'page_view') as page_views,
  count(distinct visitor_id) filter (where event_type = 'page_view') as unique_visitors,
  count(*) filter (where event_type = 'gallery_load') as gallery_load_events,
  avg(gallery_load_ms) filter (where event_type = 'gallery_load') as avg_gallery_load_ms
from public.visit_analytics
group by 1
order by 1 desc;
create or replace function public.get_visit_analytics_summary(p_visitor_id text default null)
returns jsonb
language sql
stable
security definer
set search_path = public
as $$
with page_views as (
  select *
  from public.visit_analytics
  where event_type = 'page_view'
),
visitor_location as (
  select country, region, city
  from page_views
  where visitor_id = p_visitor_id
    and (country is not null or region is not null or city is not null)
  order by created_at desc
  limit 1
),
region_summary as (
  select
    coalesce(nullif(country, ''), 'Unknown') ||
      case when city is not null and city <> '' then ' / ' || city else '' end as label,
    count(distinct visitor_id) as total
  from page_views
  group by 1
  order by total desc, label asc
  limit 12
),
device_summary as (
  select coalesce(nullif(device_type, ''), 'unknown') as label, count(distinct visitor_id) as total
  from page_views
  group by 1
  order by total desc, label asc
),
os_summary as (
  select coalesce(nullif(os_name, ''), 'Unknown') as label, count(distinct visitor_id) as total
  from page_views
  group by 1
  order by total desc, label asc
  limit 10
),
browser_summary as (
  select coalesce(nullif(browser_name, ''), 'Unknown') as label, count(distinct visitor_id) as total
  from page_views
  group by 1
  order by total desc, label asc
  limit 10
)
select jsonb_build_object(
  'total_page_views', (select count(*) from page_views),
  'unique_visitors', (select count(distinct visitor_id) from page_views),
  'visitor_location', coalesce((select jsonb_build_object('country', country, 'region', region, 'city', city) from visitor_location), '{}'::jsonb),
  'regions', coalesce((select jsonb_agg(jsonb_build_object('label', label, 'count', total) order by total desc, label asc) from region_summary), '[]'::jsonb),
  'devices', coalesce((select jsonb_agg(jsonb_build_object('label', label, 'count', total) order by total desc, label asc) from device_summary), '[]'::jsonb),
  'systems', coalesce((select jsonb_agg(jsonb_build_object('label', label, 'count', total) order by total desc, label asc) from os_summary), '[]'::jsonb),
  'browsers', coalesce((select jsonb_agg(jsonb_build_object('label', label, 'count', total) order by total desc, label asc) from browser_summary), '[]'::jsonb),
  'generated_at', now()
);
$$;

grant execute on function public.get_visit_analytics_summary(text) to anon, authenticated;
